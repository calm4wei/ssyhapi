package cn.suishou.bean.superDiscount;

public class Editor {

	private int id;
	private String icon;
	private String name;
	private String content;
	private String img;
	private String itemId;
	private String isertTimestamp;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getIsertTimestamp() {
		return isertTimestamp;
	}
	public void setIsertTimestamp(String isertTimestamp) {
		this.isertTimestamp = isertTimestamp;
	}
	
	
	
}
